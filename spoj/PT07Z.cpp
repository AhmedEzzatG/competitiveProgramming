const int N = 10009;
vector<int> adj[N];
pair<int, int> p;
void dfs(int node, int par, int dep) {
	p = max(p, { dep, node });
	for (int ch : adj[node])
		if (ch != par)
			dfs(ch, node, dep + 1);
}

